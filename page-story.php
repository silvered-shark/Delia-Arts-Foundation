                <div class="content">
                    <p class="title">A Vision for Transformation Through Music</p>
                    <p class="text">In 2018, Irish jazz musician and producer John Keating (Johno) embarked on a mission to harness the power of music as a catalyst for change. Inspired by his travels across Palestine, Sudan, North Africa, and the Middle East, Johno saw how music could heal, unite, and uplift communities facing unimaginable challenges. With this belief, the Delia Arts Foundation was born, dedicated to creating opportunities for artists and communities in the world’s most underserved regions.</p>
                </div>
                <div class="content">
                    <p class="title">First Chords: Amplifying Unheard Voices</p>
                    <p class="text">The journey began with Delia Sessions, intimate live recordings showcasing Palestinian musicians' extraordinary talents. These sessions offered global audiences a glimpse into their resilience and creativity, proving that music can transcend borders and amplify voices that might otherwise remain unheard.</p>
                </div>
                <div class="content">
                    <p class="title">Building Hope in Gaza: A Beacon for Change</p>
                    <p class="text">In 2020, the Delia Center Gaza opened its doors, becoming a sanctuary for musicians and the community. For four years, it provided free rehearsal spaces, recording studios, and training programs in a region with few opportunities for artistic expression. It became more than a space—it was a lifeline, where hope was nurtured and creativity flourished despite adversity. <br> <br> In 2021, Live for Gaza brought the world together in solidarity. Featuring icons like Roger Waters, Tom Morello, and Mohammed Assaf, this online fundraiser raised vital support for the Gaza Center while proving that music can inspire collective action across borders.</p>
                </div>
                <div class="content">
                    <p class="title">Expanding Horizons: New Projects, New Impact</p>
                    <p class="text">In Bukavu, Congo, the Delia-Ndaro Center emerged as a haven for music, art, and cultural preservation, offering a lifeline to a region deeply impacted by conflict. <br> <br> Creative Corners introduced portable multimedia stations to schools and community centers across ten countries, empowering children with music education and creativity. Delia Studios launched in Brazil and expanded to the Middle East, offering professional-grade spaces for artists to grow their talents and thrive.</p>
                </div>
                <div class="content">
                    <p class="title">Loss and Resilience: Rising From the Rubble</p>
                    <p class="text">The destruction of the Delia Center Gaza in October 2023 was a devastating blow. Yet, the spirit of resilience endured. In January 2024, the Artists Shelter in Rafah was launched, providing refuge and psychosocial music activities for displaced artists and their families. Employing local musicians to lead these efforts ensured that art remained a source of strength and healing during unimaginable hardship.</p>
                </div>
                <div class="content">
                    <p class="title">A New Chapter: Harmony Day Center Gaza</p>
                    <p class="text">In 2024, the vision of resilience and creativity took shape with the Harmony Day Center Gaza. Rising from the legacy of the Gaza Center and the Artists Shelter, this new sanctuary is dedicated to healing, connection, and cultural preservation. It offers a space where music can help children and communities recover, rebuild, and thrive amidst ongoing challenges.</p>
                </div>
                <div class="content">
                    <p class="title">Empowering Artists, Transforming Communities</p>
                    <p class="text">Artists are at the heart of this mission. They are more than creators—leaders, healers, and changemakers. Delia ensures that music remains a lifeline by empowering musicians to preserve cultural identity and foster resilience. Every note, every session, and every project is a step toward a world where art transforms lives, unites communities, and keeps hope alive.</p>
